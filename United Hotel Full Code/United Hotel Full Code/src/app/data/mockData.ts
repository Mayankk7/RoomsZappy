// ── Lookup helpers ──────────────────────────

export function getHotelById(id: string): Hotel | undefined {
  return hotels.find((hotel) => hotel.id === id);
}

export function getHotelsByDistrict(district: string): Hotel[] {
  return hotels.filter((hotel) => hotel.district === district);
}

export function getHotelsByLocation(location: string): Hotel[] {
  return hotels.filter((hotel) =>
    hotel.location.toLowerCase().includes(location.toLowerCase()),
  );
}

export function searchHotels(query: string): Hotel[] {
  const q = query.toLowerCase();
  return hotels.filter(
    (hotel) =>
      hotel.name.toLowerCase().includes(q) ||
      hotel.location.toLowerCase().includes(q) ||
      hotel.district.toLowerCase().includes(q) ||
      hotel.address.toLowerCase().includes(q),
  );
}

export function getAllDistricts(): string[] {
  return [...new Set(hotels.map((hotel) => hotel.district))];
}

export function getAllLocations(): string[] {
  return [...new Set(hotels.map((hotel) => hotel.location))];
}

export function getHotelPriceRange(
  hotelId: string,
): { min: number; max: number } | null {
  const hotel = getHotelById(hotelId);
  if (!hotel || hotel.rooms.length === 0) return null;
  const prices = hotel.rooms.map((r) => r.directPrice);
  return { min: Math.min(...prices), max: Math.max(...prices) };
}
